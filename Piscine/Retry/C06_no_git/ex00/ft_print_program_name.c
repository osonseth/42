#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

int	main(int argc, char **argv)
{
	int	len;

	len = ft_strlen(argv[0]);
	if (argc)
	{
		write(1, argv[0], len);
	}
	return (0);
}
