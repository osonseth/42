#include "libft.h"

static int	ft_abs(int n)
{
	if (n < 0)
		return (-n);
	return (n);
}

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n = ft_abs(n);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10,fd);
		ft_putnbr_fd(n % 10,fd);
	}
	else
	{
		c = n + '0';
		write(fd, &c, 1);
	}
}

